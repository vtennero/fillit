#include "libft.h"

int	ft_sqrt(int n)
{
	int	rt;

	rt = 1;
	if (n <= 0)
		return (0);
	while (rt * rt != n && rt * rt < n)
		++rt;
	return ((rt * rt == n) ? rt : 0);
}
