#include "libft.h"

int	ft_strchr_pos(const char *s, int c)
{
	int		pos;

	if (!s)
		return (-1);
	pos = 0;
	while (*s && *s != c)
	{
		++pos;
		++s;
	}
	return ((*s == '\0' && c != '\0') ? -1 : pos);
}
